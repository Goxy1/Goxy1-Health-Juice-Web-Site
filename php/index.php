            <img src="img/fruvita1.jpg" alt="sok od jabuke" class="image">
            Kada kupujete neki proizvod na primer voćni sok verovatno da ne razmišljate o tome kako on nastaje.  Logika kaže – ceđenjem voća, pogotovo ako to sugeriše natpis „100% voće“. 
                U ovom slučaju logika nije baš 100% u pravu. Ima i izuzetaka, ali takvi sokovi (od ceđenog voća) imaju kratak rok i moraju biti hlađeni. 
                Ti izuzetci- sveže ceđeni sokovi – su znatno skuplji od 120 do 150 dinara koliko košta neki uobičajni 100% voćni sok. 
                Sve se svodi na ekonomsku računicu po kojoj proizvođač soka ne želi (što je i razumljivo) da ima gubitak. 
                Kako onda oni mogu da ostvare prihod? Bilo bi sjajno kada bi bilo moguće da se sokovi proizvode tokom cele godine, a ne samo u vreme berbe voća? 
                Naravno da je moguće, tako se to u praksi i radi. Industrijski. Ja nisam tehnolog, pa ako pogrešim u mojoj analizi u nastavku teksta, molim da me neko stručan ispravi.</p>
            <br>
            <br>
            <p>Recept za proizvodnju 100% voćnog soka je sledeći: od voća se direktno ne proizvodi sok (uglavnom), već se od voća dobija sirovina za dalju preradu tzv. koncentrisani voćni sok. 
               Ali to nije tako jednostavno. Prema Pravilniku o kvalitetu voćnih sokova „Sirovine koje se koriste u proizvodnji voćnih sokova, koncentrisanih voćnih sokova i voćnih sokova u prahu su: voće, voćna kaša, koncentrisana voćna kaša, šećeri i voćna pulpa ili voćne ćelije“. 
               Prema istom pravilniku „koncentrisani voćni sok je proizvod koji se dobija od voćnog soka jedne ili više vrsta voća, fizičkim izdvajanjem određene količine prirodno sadržane vode, konzervisan isključivo fizičkim postupcima“.</p>
            <br>
            <br>
            <p>Od sirovine – koncentrisanog voćnog soka se razblaživanjem (rekonstituisanjem) sa vodom dobija 100% voćni sok.
               Dodaje se ista ona količina vode izdvojena u procesu koncentrisanja, i po potrebi limunska kiselina.
               Međutim, prema članu 29 tog istog pravilnika, stav 1 piše: „Supstance koje su neophodne za rekonstituciju proizvoda ne moraju da se navedu u deklaraciji.“ 
               Ni na jednom proizvodu ih nema, kao da i ne postoje. Nisam ni sama znala na šta se tu sve misli dok nisam pročitala Pravilnik.
            </p>
            <br>
            <br>
            <p>
            „U proizvodnji mogu da se koriste sledeće materije: pektolitički enzimi; proteolitički enzimi; amilolitički enzimi; jestivi želatin; tanini; bentonit; silicijum-dioksid, koloidni; aktivni ugalj; hemijski inertna pomoćna sredstva za filtraciju i taloženje 
            (npr. perlit, diatomejska zemlja, celuloza, nerastvorljivi poliamid, polivinilpolipirolidon, polistiren); hemijski inertna pomoćna sredstva za adsorpciju, koja se koriste za smanjenje sadržaja limonoida i naringina u 
            sokovima od citrusa i koja nemaju značajan uticaj na sadržaj limonoid glukozida, kiselina, šećera (uključujući oligosaharide) ili mineralnih materija.“
            </p>
